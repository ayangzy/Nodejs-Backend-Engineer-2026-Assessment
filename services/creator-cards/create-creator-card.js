const fs = require('fs');
const path = require('path');
const validator = require('@app-core/validator');
const { appLogger } = require('@app-core/logger');
const CreatorCard = require('@app/repository/creator-cards');
const { ACCESS_TYPE } = require('@app/models/creator-card');
const { throwAppError, ERROR_CODE } = require('@app-core/errors');
const { CreatorCardsMessages } = require('@app/messages');
const { randomBytes } = require('@app-core/randomness');

const specPath = path.resolve(__dirname, '../../specs/creator-cards/data/create-creator-card.go');
const spec = fs.readFileSync(specPath, 'utf8');
const parsedSpec = validator.parse(spec);

function slugify(text) {
  return text
    .toString()
    .toLowerCase()
    .replace(/\s+/g, '-')
    .replace(/[^\w\-]+/g, '')
    .replace(/\-\-+/g, '-')
    .replace(/^-+/, '')
    .replace(/-+$/, '');
}

async function createCreatorCard(serviceData, options = {}) {
  const data = validator.validate(serviceData, parsedSpec);
  let result;

  try {
    if (!data.access_type) {
      data.access_type = ACCESS_TYPE.PUBLIC;
    }

    if (data.access_type === ACCESS_TYPE.PRIVATE && !data.access_code) {
      throwAppError(CreatorCardsMessages.ACCESS_CODE_REQUIRED, ERROR_CODE.ACCESS_CODE_REQUIRED);
    }

    if (data.access_type === ACCESS_TYPE.PUBLIC && data.access_code) {
      throwAppError(CreatorCardsMessages.ACCESS_CODE_ON_PUBLIC, ERROR_CODE.ACCESS_CODE_ON_PUBLIC);
    }

    let isAutoGenerated = false;
    let baseSlug = '';

    if (!data.slug) {
      isAutoGenerated = true;
      baseSlug = slugify(data.title);
      data.slug = baseSlug.substring(0, 50);
    }

    const existingCard = await CreatorCard.findOne({
      query: { slug: data.slug },
    });

    if (isAutoGenerated && (existingCard || data.slug.length < 5)) {
      const randomSuffix = randomBytes(6);
      data.slug = `${baseSlug.substring(0, 43)}-${randomSuffix}`;
    } else if (existingCard) {
      throwAppError(CreatorCardsMessages.SLUG_TAKEN, ERROR_CODE.SLUG_TAKEN);
    }

    const now = Date.now();
    data.created = now;
    data.updated = now;
    data.deleted = null;

    const card = await CreatorCard.create(data);

    result = {
      id: card._id,
      title: card.title,
      description: card.description,
      slug: card.slug,
      creator_reference: card.creator_reference,
      links: card.links,
      service_rates: card.service_rates,
      status: card.status,
      access_type: card.access_type,
      access_code: card.access_code,
      created: card.created,
      updated: card.updated,
      deleted: card.deleted,
    };

    if (result.access_type === ACCESS_TYPE.PUBLIC) {
      result.access_code = null;
    }
  } catch (error) {
    appLogger.errorX(error, 'create-creator-card-error');
    throw error;
  }

  return result;
}

module.exports = createCreatorCard;
